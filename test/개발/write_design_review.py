"--- Ray Marching (80 iterations) ---\n"
"#define MAX_STEPS 80\n"
"#define MAX_DIST 20.0\n"
"#define SURF_DIST 0.001\n"
"float rayMarch(vec3 ro, vec3 rd) {\n"
"  float dO = 0.0;\n"
"  for(int i = 0; i < MAX_STEPS; i++) {\n"
"    vec3 p = ro + rd * dO;\n"
"    float dS = getDist(p);\n"
"    dO += dS;\n"
"    if(dO > MAX_DIST || abs(dS) < SURF_DIST) break;\n"
"  }\n"
"  return dO;\n"
"}\n"
"vec3 getNormal(vec3 p) {\n"
"  float d = getDist(p);\n"
"  vec2 e = vec2(0.001, 0.0);\n"
"  vec3 n = d - vec3(getDist(p-e.xyy), getDist(p-e.yxy), getDist(p-e.yyx));\n"
"  return normalize(n);\n"
"}\n"
"\n"
"--- Fluid (Advection + Divergence) ---\n"
"vec2 advect(sampler2D velocity, vec2 uv, float dt, vec2 texelSize) {\n"
"  vec2 vel = texture2D(velocity, uv).xy;\n"
"  vec2 prevPos = uv - vel * dt * texelSize;\n"
"  return texture2D(velocity, prevPos).xy;\n"
"}\n"
"float divergence(sampler2D velocity, vec2 uv, vec2 texelSize) {\n"
"  float vL = texture2D(velocity, uv - vec2(texelSize.x, 0.0)).x;\n"
"  float vR = texture2D(velocity, uv + vec2(texelSize.x, 0.0)).x;\n"
"  float vB = texture2D(velocity, uv - vec2(0.0, texelSize.y)).y;\n"
"  float vT = texture2D(velocity, uv + vec2(0.0, texelSize.y)).y;\n"
"  return 0.5 * (vR - vL + vT - vB);\n"
"}\n"
"\n"
"--- Chromatic Aberration ---\n"
"vec3 chromaticAberration(sampler2D tex, vec2 uv, float amount) {\n"
"  float r = texture2D(tex, uv + vec2(amount, 0.0)).r;\n"
"  float g = texture2D(tex, uv).g;\n"
"  float b = texture2D(tex, uv - vec2(amount, 0.0)).b;\n"
"  return vec3(r, g, b);\n"
"}\n"
"\n"
"--- Barrel Distortion ---\n"
"vec2 barrelDistortion(vec2 uv, float strength) {\n"
"  vec2 p = uv - 0.5;\n"
"  float r2 = dot(p, p);\n"
"  return uv + p * (strength * r2);\n"
"}\n"
