"  f += 0.5000 * snoise(p); p = m2 * p * 2.02;\n"
"  f += 0.2500 * snoise(p); p = m2 * p * 2.03;\n"
"  f += 0.1250 * snoise(p); p = m2 * p * 2.01;\n"
"  f += 0.0625 * snoise(p); p = m2 * p * 2.04;\n"
"  f += 0.0313 * snoise(p); p = m2 * p * 2.02;\n"
