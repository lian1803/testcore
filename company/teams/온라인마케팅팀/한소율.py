import os
import anthropic

MODEL = "claude-sonnet-4-5"

SYSTEM_PROMPT = """너는 한소율이야. 온라인마케팅팀의 세일즈 매니저 — 콜드메일·DM 영업, 리드 너처링, 계약 클로징 전담.
전문 분야: B2B 콜드 아웃리치, 이메일 시퀀스 설계, DM 세일즈 스크립트, CRM 파이프라인 관리, 견적서·계약서 초안 작성

핵심 원칙:
- 첫 콜드메일은 셀러의 구체적 페인포인트 데이터(리드 카드 기반)를 반드시 인용해서 '당신을 분석했다'는 신뢰감을 준다 — 일반적 템플릿 금지
- 발송 전 모든 메일·DM 초안은 리안 최종 승인을 받는다. 승인 없이 대외 발송 절대 금지
- 견적은 항상 원가 + 30% 최소 마진을 보장하며, 패키지 3단계(베이직/스탠다드/프리미엄)로 제시하여 업셀 구조를 만든다

결과물: 1) 콜드메일 시퀀스 (1차·2차·3차 메일 본문+제목), 2) DM 스크립트 (첫 접근·후속·클로징), 3) 견적서 (패키지별 항목·단가·원가·마진율·총액), 4) 계약서 초안 (SOW 포함), 5) 파이프라인 현황표 (리드별 상태·다음 액션·담당·예정일)

절대 금지:
- 두루뭉술한 조언
- "이럴 수도 있고 저럴 수도 있어요"
- 이론만 나열
항상: 실제 수집 데이터 기반 수치만. 추정치는 "(추정: 산출근거)" 형식 필수. 출처 없는 시장 통계·성장률·사례 생성 = 감점. 바로 복붙 가능한 형식으로"""

def run(context: dict, client: anthropic.Anthropic) -> str:
    print("\n" + "="*60)
    print("🤖 한소율 | 세일즈 매니저 — 콜드메일·DM 영업, 리드 너처링, 계약 클로징 전담")
    print("="*60)

    leads = context.get('서진혁', '')
    strategy = context.get('윤채원', '')
    creative = context.get('박시우', '')

    user_msg = f"""업무: {context['task']}

서진혁 리드 카드:
{leads[:2500]}

윤채원 전략:
{strategy[:2500]}

박시우 크리에이티브:
{creative[:2500]}

위 데이터에서 A등급 최상위 셀러 1개를 선택해서 완성도 있게 만들어줘.
(A등급 없으면 가장 가능성 높은 1개 선택. 선택한 셀러명을 첫 줄에 명시.)

⚠️ 발신 주체 규칙:
- 발신 주체는 반드시 리안컴퍼니 (클라이언트 사칭 절대 금지)
- "저희 [셀러명]에서..." 이런 표현 금지 — 셀러는 우리 고객이지 우리 팀이 아님

⚠️ 절대 금지 (수치 정직성):
- 없는 수치 만들지 마: "19배 증가", "98배 증가", "YoY +340%" 전부 금지
- 가짜 사례: "셀러 17곳 도왔습니다" — 실제 사례 없으면 쓰지 마
- 출처 없는 시장 데이터 금지
- 근거 없는 긴급성 조장 금지
- 추정 수치는 반드시 "(추정)" 명시

✅ 허용되는 실제 데이터:
- 서진혁 리드 카드에 있는 실제 수집 데이터만 사용
- "팔로워 대비 리뷰 부족" 같은 관찰 기반 사실
- "상세페이지 개선 시 전환율 올라가는 경향" (업계 일반 사실)

✅ 반드시 완성 (아래 4가지 전부, 견적서는 이도현이 별도 처리):

1. 콜드메일 (1차/2차만) — 제목 + 본문 전문 (완성본)
   - 서진혁 리드 카드 실제 데이터만 인용
   - ⚠️ 메일에 구체적 금액/견적 절대 금지. "맞춤 견적서를 별도 보내드리겠습니다" 정도만
   - 가짜 사례/수치 금지 ("전환율 250% 상승" 등 출처 없는 외부 사례 금지)
   - 연락처: 스마트스토어 '문의하기' 또는 공개 이메일. 빈 플레이스홀더 금지 — 발송 채널 구체적으로 명시

2. 인스타 DM 스크립트 3단계 (각 3줄 이내):
   - 1단계: 첫 접촉 (관심 유도)
   - 2단계: 무료 진단 제안
   - 3단계: 미팅/통화 설정

3. 클로징 스크립트 (전화/줌 미팅용, 5단계 각 1-2줄):
   인사 → 현황 확인 → 솔루션 제시 → 패키지 안내(금액은 이도현 견적서 참조) → 다음 단계 합의

4. 발송 타이밍 표 (채널/요일/시간/이유) — 3줄이면 충분

마지막 줄까지 완성. 잘리면 무효.
콜드메일/DM은 공개 연락처 또는 수신동의 고객에게만 발송."""

    full_response = ""
    with client.messages.stream(
        model=MODEL,
        max_tokens=5000,
        messages=[{"role": "user", "content": user_msg}],
        system=SYSTEM_PROMPT,
    ) as stream:
        for text in stream.text_stream:
            print(text, end="", flush=True)
            full_response += text

    print()
    return full_response
