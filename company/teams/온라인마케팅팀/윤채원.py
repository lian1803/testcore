import os
import anthropic

MODEL = "claude-sonnet-4-5"

SYSTEM_PROMPT = """너는 윤채원이야. 온라인마케팅팀의 마케팅 전략가 — 클라이언트별 맞춤 마케팅 전략 수립 및 서비스 패키지 설계.
전문 분야: 네이버 SEO/키워드 전략, 쿠팡 광고 최적화, 메타(인스타/페이스북) 광고 전략, 상세페이지 UX 전략, 콘텐츠 마케팅 퍼널 설계

핵심 원칙:
- 전략은 반드시 셀러의 현재 단계(론칭/성장/성숙)와 카테고리 경쟁도를 기반으로 커스터마이징한다 — 모든 클라이언트에 같은 패키지를 복붙하지 않는다
- 모든 전략 제안에는 예상 KPI(검색 노출 수, 클릭률, 전환율, 리뷰 증가 수)와 예상 ROI를 숫자로 제시한다
- 실행사 연동 서비스(체험단/구매평/리워드)의 원가를 항상 최신으로 파악하고, 전략 안에 원가·마진·클라이언트 청구 금액을 명시한다

결과물: 클라이언트별 마케팅 전략서: {현황 진단 요약, 목표 KPI, 추천 서비스 조합(SEO+체험단+광고 등), 월별 실행 로드맵, 예상 비용(원가/마진/청구가), 예상 성과, 리스크 및 대응책}

절대 금지:
- 두루뭉술한 조언
- "이럴 수도 있고 저럴 수도 있어요"
- 이론만 나열
항상: 실제 수집 데이터 기반 수치만. 추정치는 "(추정: 산출근거)" 형식 필수. 출처 없는 시장 통계·성장률·사례 생성 = 감점. 바로 복붙 가능한 형식으로"""

def run(context: dict, client: anthropic.Anthropic) -> str:
    print("\n" + "="*60)
    print("🤖 윤채원 | 마케팅 전략가 — 클라이언트별 맞춤 마케팅 전략 수립 및 서비스 패키지 설계")
    print("="*60)

    leads = context.get('서진혁', '')

    user_msg = f"""업무: {context['task']}

서진혁이 발굴한 리드 카드:
{leads[:5000]}

위 리드 카드에서 A등급 상위 1개를 자동 선택해서 그 셀러에 집중한 완성도 있는 전략을 짜줘.
A등급이 없으면 B등급 1위를 선택. 선택한 셀러명을 전략서 첫 줄에 반드시 명시할 것.

⚠️ 수치 정직성:
- 없는 수치 만들지 마. 출처 없는 시장 데이터 금지
- 추정이면 "(추정치: 산출근거)" 명시
- 리드 카드에 있는 실제 수집 데이터만 사용. 없는 수치 지어내기 금지
- "실제 확인 결과" 표현 금지 — 크롤러가 수집하지 않은 데이터에 쓰면 데이터 위조. "(추정)" 또는 "(직접 조회 필요)"만 허용

✅ 전략 수립 기준 (리드 카드에서 동적으로 읽어서 반영):
- 셀러의 실제 플랫폼 + 카테고리 + 페인포인트 3가지 기반으로 차별화 전략 수립
- "일반적인 체험단+SEO 루틴" 표 1개로 요약 후, 이 제품/카테고리에만 통하는 독창적 채널 2개 이상 제안
- 독창적 채널은 리드 카드의 카테고리·타겟·판매 플랫폼에 맞게 선택 (범용 템플릿 복붙 금지)

✅ 반드시 포함 (완성본):
- 현황 진단: 리드 카드 실제 데이터 기반
- 차별화 포지셔닝 전략 (이 셀러/제품에만 통하는 것)
- 베이직/스탠다드/프리미엄 패키지 3개 — **서비스 내용만** (뭘 하는지)
  - ⚠️ 원가/판매가/마진 금액은 이도현이 단독 결정. 여기서 금액 절대 쓰지 마.
  - 각 패키지에 포함되는 서비스 항목 리스트만 작성.
  - **3개 패키지 전부 완성해. 패키지 C 빠지면 무효.**
- KPI 목표 (보수적 추정, "(추정: 산출근거)" 형식 필수)

⚠️ 절대 금지:
- HTML 코드 (img, div, style 등 전부)
- 확인 안 된 셀러 배경 가정 (직접 조회하지 않은 사실을 마케팅 카피에 쓰면 허위광고)
- 근거 없는 확률/통계 ("1위 달성 가능성 80%" 등)
- 매출 추정 시 산출식 없는 숫자: 반드시 "팔로워×전환율×객단가 = 결과값" 형식

⚠️ 독창적 채널 2개 (점수의 25%):
이 셀러의 실제 카테고리/타겟에 맞는 채널 2개를 직접 골라서:
- 왜 이 제품에 효과적인지
- 구체적 실행 계획 (누가, 어디서, 어떻게)
을 작성. 차별화 핵심이 프리미엄에만 묶이지 않도록 스탠다드에도 미니 버전 포함.

패키지 C(프리미엄)까지 완성 후 잘리면 무효. 마크다운 텍스트와 표로만."""

    full_response = ""
    with client.messages.stream(
        model=MODEL,
        max_tokens=6000,
        messages=[{"role": "user", "content": user_msg}],
        system=SYSTEM_PROMPT,
    ) as stream:
        for text in stream.text_stream:
            print(text, end="", flush=True)
            full_response += text

    print()
    return full_response
