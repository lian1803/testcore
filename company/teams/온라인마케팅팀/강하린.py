import os
import anthropic
from core.models import CLAUDE_HAIKU

MODEL = CLAUDE_HAIKU

SYSTEM_PROMPT = """너는 강하린이야. 온라인마케팅팀의 그로스 애널리스트 — 성과 추적, 데이터 분석, 개선안 도출, 피드백 루프 운영.
전문 분야: 이커머스 데이터 분석, A/B 테스트 설계, 마케팅 채널별 ROI 분석, 고객 이탈 방지 전략, 업셀/크로스셀 기회 발굴

핵심 원칙:
- 모든 서비스 시행 전후에 반드시 수치를 측정한다 — '체감상 좋아졌다'는 보고 금지, 숫자로만 말한다
- 매주 성과 리뷰를 통해 전략가·크리에이티브 디렉터에게 구체적 개선 포인트를 전달한다 — '더 잘해달라'가 아닌 '이 키워드의 CTR이 2.1%→1.3%로 하락, 제목 변경 테스트 필요' 수준으로
- 클라이언트별 3개월 성과 트렌드를 추적하여 업셀 기회(추가 서비스 제안)와 이탈 위험(성과 저하·응답 둔화)을 사전에 감지한다

결과물: 1) 주간 성과 대시보드 (클라이언트별 핵심 KPI 전주 대비 변화), 2) 월간 성과 분석 리포트 (채널별 ROI, 서비스별 효과 분석, 개선 권고사항), 3) A/B 테스트 결과 보고 (가설·결과·의사결정·다음 테스트), 4) 클라이언트 헬스 스코어 (이탈위험/안정/성장 분류 + 업셀 기회 플래그)

절대 금지:
- 두루뭉술한 조언
- "이럴 수도 있고 저럴 수도 있어요"
- 이론만 나열
항상: 실제 수집 데이터 기반 수치만. 추정치는 "(추정: 산출근거)" 형식 필수. 출처 없는 시장 통계·성장률·사례 생성 = 감점. 바로 복붙 가능한 형식으로

발신 주체 규칙:
- 콜드메일/DM의 발신 주체를 클라이언트 회사로 사칭 절대 금지
- 발신 주체는 반드시 "리안컴퍼니" 또는 "LIANCP"
- "저희 [클라이언트명]에서..." 이런 표현 금지
- 올바른 예: "안녕하세요, 마케팅 전문 리안컴퍼니입니다. [클라이언트명] 스토어를 분석해봤는데요..."

A/B 테스트 규칙:
- 테스트 제목/본문에 허위 수치 삽입 절대 금지
- 실제 데이터에 없는 수치 사용 금지 (예: "캠핑장 50곳", "리뷰 4.8점" 등)
- 수치를 넣으려면 서진혁/윤채원 데이터에서 가져올 것
- 허위 수치 발견 시 해당 A/B 테스트 세트 전체 무효"""

def run(context: dict, client: anthropic.Anthropic) -> str:
    print("\n" + "="*60)
    print("🤖 강하린 | 그로스 애널리스트 — 성과 추적, 데이터 분석, 개선안 도출, 피드백 루프 운영")
    print("="*60)

    user_msg = f"""업무: {context['task']}

[서진혁 리드 카드]
{context.get('서진혁', '')[:1000]}

[윤채원 전략]
{context.get('윤채원', '')[:1500]}

위 데이터에서 선택된 셀러 기반으로 아래 3가지를 만들어줘. 복잡한 월간 루틴 불필요.

⚠️ 역할 범위 엄수:
- 콜드메일/영업 메일/DM 작성 절대 금지 — 그건 한소율 역할
- 발신 주체를 클라이언트 회사로 사칭 절대 금지 — 발신자는 항상 리안컴퍼니
- 결과물에 [1][2][3] 각주 형식 사용 금지

반드시 완성 (절대 잘라내지 마):

## 1. 주간 대시보드 템플릿 — (목표 시뮬레이션, 실제 데이터로 교체 필요)

| 주차 | 노출수(목표) | CTR(%)(목표) | 리뷰수(목표) | 주간 핵심 액션 |
|------|------------|-------------|-------------|--------------|
| W1 | (숫자) | (숫자) | 현재→목표 | (구체적 액션) |
| W2 | (숫자) | (숫자) | (목표) | (구체적 액션) |
| W3 | (숫자) | (숫자) | (목표) | (구체적 액션) |
| W4 | (숫자) | (숫자) | (목표) | (구체적 액션) |

현실적 목표로 채워줘. 자동 경고 수식: =IF(D2<목표,"⚠️미달","정상")

## 2. A/B 테스트 계획 3개 (각각 4가지 전부 완성, 서진혁/윤채원 실제 데이터만 사용)

**테스트 1: 상세페이지 헤드라인 A vs B**
- 가설: 리드 카드 실제 카테고리/제품 기반 헤드라인 2안 비교
- 측정: CTR(%) — 상세페이지 클릭률
- 기간: 14일
- 판단: 0.3%p 이상 차이 나는 쪽 채택

**테스트 2: 체험단 리뷰 인센티브 A vs B**
- 가설: A안 vs B안 (구체적 인센티브 설명)
- 측정: 리뷰 작성 완료율(%)
- 기간: 14일 (5명씩)
- 판단: 15%p 이상 차이 나는 쪽 채택

**테스트 3: 콘텐츠 스타일 A vs B**
- 가설: A안(정보형) vs B안(감성형)
- 측정: 저장률(%)
- 기간: 14일
- 판단: 저장률 2%p 이상 차이 나는 쪽 채택

⚠️ 테스트 제목/내용에 리드 카드에 없는 수치 삽입 절대 금지.
수치를 넣으려면 반드시 서진혁/윤채원 데이터에서 가져올 것.

## 3. 이탈 감지 트리거 (3가지, 각 1줄)

| 지표 | 이탈 경보 조건 | 트리거 시 액션 |
|------|-------------|-------------|
| 리뷰 | (구체적 기준) | (구체적 대응) |
| 콜드메일 오픈율 | (구체적 기준) | (구체적 대응) |
| DM 응답률 | (구체적 기준) | (구체적 대응) |

마지막 줄까지 완성해. 잘리면 무효."""

    full_response = ""
    with client.messages.stream(
        model=MODEL,
        max_tokens=3000,
        messages=[{"role": "user", "content": user_msg}],
        system=SYSTEM_PROMPT,
    ) as stream:
        for text in stream.text_stream:
            print(text, end="", flush=True)
            full_response += text

    print()
    return full_response
